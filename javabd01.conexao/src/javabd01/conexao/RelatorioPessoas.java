    public void preencherTabela(){
        PessoaDAO pDAO = new PessoaDAO();
        List<Pessoa> lp = pDAO.getPessoas();
        
        DefaultTableModel tabelaPessoas = (DefaultTableModel) tblPessoa.getModel();
        
        for (Pessoa p : lp){
            Object[] o = new Object[]{
                p.getId(),
                p.getNome(),
                p.getSexo(),
                p.getIdioma(),
            };
            tabelaPessoas.addRow(o);
        }
    }
    
